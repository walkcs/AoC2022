def stratGuide2(sample):
    l = sample.split('\n')

    scoreDict = {'A': 1, 'B': 2, 'C': 3}
    convDict = {'X': 'A', 'Y': 'B', 'Z': 'C'}
    winDict = {'A': 'B','B': 'C','C': 'A'}
    lossDict = {'B': 'A','C': 'B','A': 'C'}
    wld = {'X': 0, 'Y': 3, 'Z': 6}

    totalScore = 0
    for k in l:
        t = k[0]
        winPts = wld[k[-1]]
        if winPts==3: #draw
            s = scoreDict[t]+3
        elif winPts==6: # win
            toThrow = winDict[t]
            s = scoreDict[toThrow]+6
        else:
            toThrow = lossDict[t]
            s = scoreDict[toThrow]
        totalScore += s
    return totalScore
